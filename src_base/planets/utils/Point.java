package planets.utils;

/**
 *  Just a Point object used for geometry
 * 
 * @author Adri
 */
public class Point {
    /**
     * x coordinate of the point
     */
    public double x;
    /**
     * x coordinate of the point
     */
    public double y;
    
    /**
     * Basic constructor
     * 
     * @param x the x coordinate
     * @param y  the y coordinate
     */
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    } 
}
